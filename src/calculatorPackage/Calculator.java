package calculatorPackage;

public class Calculator {
    //Addition Method
    public Integer add(int num1, int num2) {
        return num1 + num2;
    }
    //Subtraction Method
    public Integer subtract( int num1, int num2) {
        return num1 - num2;
    }
    //Multiplication Method
    public Integer multiply(int num1, int num2) {
        return num1 * num2;
    }
    //Division Method
    public Integer divide(int num1, int num2) {
        return num1/num2;
    }
    //Square Method
    public Integer square(int num1) {
        return num1*num1;
    }
}


